  public void cacheClear(int group)
  {
    for (int index = 0; index < 100; ++index)
    {
      int cachedFlag = (int) this.m_cachedFlags[index];
      if ((cachedFlag & 16) == 0 && (group == 0 || (cachedFlag & 7) == group))
        this.cacheUnload(index);
    }
  }

  public void cacheUnload(int index)
  {
    int num = (int) this.m_cachedFlags[index] & 7;
    if (num != 0)
      --this.m_cachedGroupCounts[num - 1];
    this.m_cachedFlags[index] = (byte) 0;
    this.m_cachedIDs[index] = (short) -1;
    Object3D[] cachedResource = this.m_cachedResources[index];
    for (int index1 = 0; index1 < cachedResource.Length; ++index1)
