  public void scaleRGB(
    ref int[] srcRGB,
    int srcX,
    int srcY,
    int srcWidth,
    int srcHeight,
    ref int[] destRGB,
    int destX,
    int destY,
    int destWidth,
    int destHeight)
  {
    int[] scaleBufferRgb = this.m_scaleBufferRGB;
    int length = srcWidth;
    int src_position = srcY * length + srcX;
    int num1 = srcWidth;
    int dst_position = destY * num1 + srcX;
    int num2 = srcHeight;
    int num3 = srcHeight << 1;
    int num4 = destHeight << 1;
    int num5 = destHeight;
    if (srcHeight < destHeight)
    {
      while (destY < num5)
      {
        midp.JSystem.arraycopy((Array) srcRGB, src_position, (Array) scaleBufferRgb, dst_position, length);
        num2 += num3;
        if (num2 >= num4)
        {
          num2 -= num4;
          ++srcY;
          src_position += length;
        }
        ++destY;
        dst_position += num1;
      }
    }
    else
    {
      while (destY < num5)
      {
        num2 += num4;
        if (num2 > num3)
        {
          midp.JSystem.arraycopy((Array) srcRGB, src_position, (Array) scaleBufferRgb, dst_position, length);
          ++destY;
          dst_position += num1;
          num2 -= num3;
        }
        ++srcY;
        src_position += length;
      }
    }
    int num6 = srcWidth;
    int num7 = srcX;
    int num8 = destWidth;
    int num9 = destX;
    int num10 = srcWidth;
    int num11 = srcWidth << 1;
    int num12 = destWidth << 1;
    int num13 = destWidth;
    int num14 = destWidth * destHeight;
    if (srcWidth < destWidth)
    {
      while (destX < num13)
      {
        int index1 = num7;
        for (int index2 = num9; index2 < num14; index2 += num8)
        {
          destRGB[index2] = scaleBufferRgb[index1];
          index1 += num6;
        }
        num10 += num11;
        if (num10 >= num12)
        {
          num10 -= num12;
          ++srcX;
          ++num7;
        }
        ++destX;
        ++num9;
      }
    }
    else
    {
      while (destX < num13)
      {
        num10 += num12;
        if (num10 > num11)
        {
          int index1 = num7;
          for (int index2 = num9; index2 < num14; index2 += num8)
          {
            destRGB[index2] = scaleBufferRgb[index1];
            index1 += num6;
          }
          ++destX;
          ++num9;
          num10 -= num11;
        }
        ++srcX;
        ++num7;
      }
    }
  }

